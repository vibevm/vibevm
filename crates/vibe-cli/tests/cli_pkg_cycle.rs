// Note: M1.15's `--rev <commit-sha>` path requires `git archive
// --remote=<url> <sha>` to resolve a non-ref-named commit, which the
// `git` upload-archive protocol does NOT support over `file://` (or
// over the smart HTTP transport). It only works against hosts that
// expose loose objects — which in practice means a real git server
// (GitHub, GitLab, etc.). The hermetic e2e cannot exercise `--rev`
// against a `file://` bare repo. Production smoke walk against
// GitHub covers this path; see `manual-tests/m1.15-git-source.md`.

/// A repeat `vibe install` (no args, re-resolving `[requires]`) of a
/// git-source declaration is an idempotent success under the loading
/// model — it re-resolves the git source and re-materialises the
/// `vibedeps/` slot. The manifest keeps exactly one git-source entry
/// and the lockfile one entry with `source_kind = "git"`.
#[test]
#[verifies("spec://vibevm/modules/vibe-registry/PROP-002#git-source", r = 1)]
fn install_git_source_then_repeat_install_no_args_is_idempotent() {
    if !git_available() {
        eprintln!("skipping install_git_source_then_repeat_install: git not on PATH");
        return;
    }
    let outer = tempfile::tempdir().unwrap();
    let bare = make_single_package_bare_repo(outer.path());
    let cache = outer.path().join("cache");
    fs::create_dir_all(&cache).unwrap();

    let project = tempfile::tempdir().unwrap();
    init_project(project.path());
    let url = format!("file://{}", bare.to_string_lossy().replace('\\', "/"));

    vibe()
        .env("VIBE_REGISTRY_CACHE", &cache)
        .arg("install")
        .arg("org.vibevm.world/wal")
        .arg("--git")
        .arg(&url)
        .arg("--tag")
        .arg("v0.2.0")
        .arg("--path")
        .arg(project.path())
        .arg("--assume-yes")
        .assert()
        .success();

    // Repeat install with no arguments — re-resolves from `[requires]`
    // and re-materialises. Idempotent: succeeds without error.
    vibe()
        .env("VIBE_REGISTRY_CACHE", &cache)
        .arg("install")
        .arg("--path")
        .arg(project.path())
        .arg("--assume-yes")
        .assert()
        .success();

    // Manifest unchanged — still one git-source entry; no duplicate
    // wrote into either list during the re-install.
    let manifest_text = fs::read_to_string(project.path().join("vibe.toml")).unwrap();
    let manifest =
        vibe_core::manifest::Manifest::parse_str(&manifest_text).expect("vibe.toml round-trips");
    assert_eq!(manifest.requires.git_packages.len(), 1);
    assert!(manifest.requires.packages.is_empty());

    // Lockfile still has one entry, source_kind = Git, and the
    // `vibedeps/` slot is present.
    let lock_text = fs::read_to_string(project.path().join("vibe.lock")).unwrap();
    let lock: vibe_core::manifest::Lockfile = toml::from_str(&lock_text).unwrap();
    assert_eq!(lock.packages.len(), 1);
    assert_eq!(
        lock.packages[0].source_kind,
        Some(vibe_core::manifest::SourceKind::Git)
    );
    assert!(
        project
            .path()
            .join("vibedeps/flow-wal/0.2.0/vibe.toml")
            .is_file()
    );
}

