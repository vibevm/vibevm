    let g = &manifest.requires.git_packages[0];
    assert!(
        matches!(&g.ref_kind, vibe_core::manifest::GitRefKind::Branch(b) if b == "main"),
        "expected branch refkind, got: {:?}",
        g.ref_kind
    );

    let lock_text = fs::read_to_string(project.path().join("vibe.lock")).unwrap();
