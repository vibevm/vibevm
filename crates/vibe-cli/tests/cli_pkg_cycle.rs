//! End-to-end tests for the full M0 walk: init → install → list → uninstall.
//!
//! The `wal` package exercised here is the real `org.vibevm.world/wal` that
//! ships in this repo at `packages/org.vibevm.world/wal/` — the tests dogfood
//! the actual product package, not a hand-written fixture. Also covers the
//! M1.1 git-backed-registry install and the M1.15 git-source installs (`--git`
//! + `--tag` / `--branch`).

mod common;

use std::fs;

use common::{
    fixture_registry, git_available, init_project, make_per_package_registry,
    make_single_package_bare_repo, make_wal_dir_registry, vibe,
    write_project_with_per_package_registry,
};
use predicates::prelude::*;
use specmark::verifies;

#[test]
#[verifies("spec://vibevm/VIBEVM-SPEC#install-workflow-in-detail")]
fn full_install_cycle() {
    // PROP-009 loading model: `vibe install` materialises a resolved
    // package's published tree verbatim into a `vibedeps/<kind>-<name>/
    // <version>/` slot at the workspace root, then generates the boot
    // artifacts (`spec/boot/INDEX.md`, the `CLAUDE.md` redirect).
    // `vibe uninstall` removes the slot and regenerates boot. There are
    // no longer any mirrored `spec/flows/<kind>/<name>/*` files in the
    // project tree, and no `NN-` boot snippet placed directly in
    // `spec/boot/`.
    let project = tempfile::tempdir().unwrap();
    init_project(project.path());

    // Install org.vibevm.world/wal from a directory registry seeded with
    // the real package tree (dogfood, not a fixture).
    vibe()
        .arg("install")
        .arg("org.vibevm.world/wal")
        .arg("--path")
        .arg(project.path())
        .arg("--registry")
        .arg(make_wal_dir_registry(project.path()))
        .arg("--assume-yes")
        .assert()
        .success();

    // The package's whole published tree is materialised verbatim into
    // its `vibedeps/` slot — the real org.vibevm.world/wal v0.2.0 ships
    // `vibe.toml`, `README.md`, `spec/boot/10-flow-wal.md`, and
    // `spec/flows/wal/*.md` files (plus a skill and LICENSE, not asserted
    // here).
    let slot = project.path().join("vibedeps/flow-wal/0.2.0");
    for rel in [
        "vibe.toml",
        "README.md",
        "spec/boot/10-flow-wal.md",
        "spec/flows/wal/WAL-PROTOCOL.md",
        "spec/flows/wal/session-end-hook.md",
        "spec/flows/wal/morning-routine.md",
    ] {
        assert!(
            slot.join(rel).is_file(),
            "expected `vibedeps/flow-wal/0.2.0/{rel}` after install"
        );
    }
    // The OLD mirror paths must NOT exist any more.
    assert!(
        !project
            .path()
            .join("spec/flows/wal/WAL-PROTOCOL.md")
            .exists(),
        "the legacy [writes] mirror layout is retired — no project-tree mirror"
    );
    assert!(
        !project.path().join("spec/boot/10-flow-wal.md").exists(),
        "the NN- boot-snippet placement is retired"
    );

    // User-owned file survived untouched.
    let core_before = fs::read_to_string(project.path().join("spec/boot/00-core.md")).unwrap();

    // The generated boot manifest exists and is a `schema = 1` TOML
    // document listing the node's own foundation boot.
    let index_path = project.path().join("spec/boot/INDEX.md");
    assert!(index_path.is_file(), "spec/boot/INDEX.md must be generated");
    let index = fs::read_to_string(&index_path).unwrap();
    let index_toml: toml::Value = toml::from_str(&index).expect("INDEX.md is TOML");
    assert_eq!(
        index_toml.get("schema").and_then(|v| v.as_integer()),
        Some(1)
    );
    assert!(
        index.contains("spec/boot/00-core.md"),
        "INDEX.md must name the node's own foundation boot:\n{index}"
    );
    // Regression guard: a CLI `vibe install <pkgref>` must compose the
    // freshly-installed package's boot into INDEX.md — the manifest
    // `[requires]` is merged before the boot artifacts are regenerated.
    assert!(
        index.contains("vibedeps/flow-wal/0.2.0/spec/boot/10-flow-wal.md"),
        "INDEX.md must name the installed dependency's boot snippet:\n{index}"
    );
    // The fixtures declare no `link = "static"` dependency, so there is
    // no STATIC.md.
    assert!(
        !project.path().join("spec/boot/STATIC.md").exists(),
        "STATIC.md is only generated for static-linked dependencies"
    );
    // The redirect files are generated thin pointers at the boot
    // artifacts.
    for redirect in ["CLAUDE.md", "AGENTS.md", "GEMINI.md"] {
        let body = fs::read_to_string(project.path().join(redirect)).unwrap();
        assert!(
            body.contains("Generated by vibe") && body.contains("spec/boot/INDEX.md"),
            "{redirect} must be a generated redirect pointing at the boot artifacts"
        );
    }

    // Lockfile must now carry the entry — but with the PROP-009 shape:
    // `files_written` is empty (the footprint is the deterministic
    // `vibedeps/` slot) and there is no `boot_snippet` filename.
    let lock_text = fs::read_to_string(project.path().join("vibe.lock")).unwrap();
    let lock: vibe_core::manifest::Lockfile = toml::from_str(&lock_text).unwrap();
    assert_eq!(lock.packages.len(), 1);
    assert_eq!(lock.packages[0].name, "wal");
    assert_eq!(lock.packages[0].version.to_string(), "0.2.0");
    assert!(lock.packages[0].content_hash.starts_with("sha256:"));
    assert!(
        lock.packages[0].files_written.is_empty(),
        "files_written is empty under the loading model"
    );
    assert!(
        lock.packages[0].boot_snippet.is_none(),
        "the lockfile no longer records a boot_snippet filename"
    );

    // Cache directory populated.
    assert!(
        project
            .path()
            .join(".vibe/cache/org.vibevm.world/wal/v0.2.0/vibe.toml")
            .is_file()
    );

    // `vibe list` reflects the install.
    vibe()
        .arg("list")
        .arg("--path")
        .arg(project.path())
        .assert()
        .success()
        .stdout(predicate::str::contains("flow"))
        .stdout(predicate::str::contains("wal"))
        .stdout(predicate::str::contains("0.2.0"));

    // `vibe uninstall` removes the package's `vibedeps/` slot.
    vibe()
        .arg("uninstall")
        .arg("org.vibevm.world/wal")
        .arg("--path")
        .arg(project.path())
        .arg("--assume-yes")
        .assert()
        .success();

    assert!(
        !slot.exists(),
        "the vibedeps/ slot must be gone after uninstall"
    );
    // INDEX.md is regenerated and still present — boot is recomputed
    // from the now-empty resolution.
    assert!(index_path.is_file(), "INDEX.md is regenerated on uninstall");

    // User-owned file still intact.
    let core_after = fs::read_to_string(project.path().join("spec/boot/00-core.md")).unwrap();
    assert_eq!(core_before, core_after);

    // Lockfile entry removed.
    let lock_text = fs::read_to_string(project.path().join("vibe.lock")).unwrap();
    let lock: vibe_core::manifest::Lockfile = toml::from_str(&lock_text).unwrap();
    assert!(lock.packages.is_empty());

    // `list` after uninstall shows no packages.
    vibe()
        .arg("list")
        .arg("--path")
        .arg(project.path())
        .assert()
        .success()
        .stdout(predicate::str::contains("no packages"));
}

/// A second `vibe install` of the same package is an idempotent
/// success, not a rejection. The depsolver still runs — a CLI pkgref
/// always takes the full path — but PROP-011 §2.3 skips re-copying the
/// `vibedeps/` slot already present for the resolved version. The slot
/// still stands and the lockfile keeps exactly one entry.
#[test]
#[verifies("spec://vibevm/VIBEVM-SPEC#install-workflow-in-detail")]
fn install_second_install_is_idempotent() {
    let project = tempfile::tempdir().unwrap();
    init_project(project.path());

    vibe()
        .arg("install")
        .arg("org.vibevm.world/wal")
        .arg("--path")
        .arg(project.path())
        .arg("--registry")
        .arg(make_wal_dir_registry(project.path()))
        .arg("--assume-yes")
        .assert()
        .success();

    // Second install of the same package succeeds — the slot is already
    // present for 0.2.0, so materialisation skips it (PROP-011 §2.3).
    vibe()
        .arg("install")
        .arg("org.vibevm.world/wal")
        .arg("--path")
        .arg(project.path())
        .arg("--registry")
        .arg(make_wal_dir_registry(project.path()))
        .arg("--assume-yes")
        .assert()
        .success();

    // The slot is still present and the lockfile still carries exactly
    // one entry — no duplicate, no drift.
    assert!(
        project
            .path()
            .join("vibedeps/flow-wal/0.2.0/vibe.toml")
            .is_file(),
        "vibedeps/ slot must survive a re-install"
    );
    let lock: vibe_core::manifest::Lockfile =
        toml::from_str(&fs::read_to_string(project.path().join("vibe.lock")).unwrap()).unwrap();
    assert_eq!(lock.packages.len(), 1);
    assert_eq!(lock.packages[0].name, "wal");
}

/// PROP-011 §2.3 — materialise only the diff. A re-install whose slot
/// is already present for the resolved version skips the re-copy: the
/// install report lists the slot under `skipped`, not `materialised`.
#[test]
fn install_skips_a_present_slot_on_re_install() {
    let project = tempfile::tempdir().unwrap();
    // The registry lives OUTSIDE the project so a re-install's freshness
    // scan never sees it; built once and reused across both installs (a
    // second `make_wal_dir_registry` would re-copy the tree and look like
    // a change).
    let reg_home = tempfile::tempdir().unwrap();
    let registry = make_wal_dir_registry(reg_home.path());
    init_project(project.path());

    // First install materialises the `vibedeps/flow-wal/0.2.0` slot.
    vibe()
        .arg("install")
        .arg("org.vibevm.world/wal")
        .arg("--path")
        .arg(project.path())
        .arg("--registry")
        .arg(&registry)
        .arg("--assume-yes")
        .assert()
        .success();

    // Second install — same pkgref, full pipeline — but the slot is
    // already present for the resolved version, so the copy is skipped.
    let out = vibe()
        .arg("--json")
        .arg("install")
        .arg("org.vibevm.world/wal")
        .arg("--path")
        .arg(project.path())
        .arg("--registry")
        .arg(&registry)
        .arg("--assume-yes")
        .output()
        .unwrap();
    assert!(out.status.success());
    let stdout = String::from_utf8(out.stdout).unwrap();
    let docs: Vec<serde_json::Value> = serde_json::Deserializer::from_str(&stdout)
        .into_iter::<serde_json::Value>()
        .collect::<Result<_, _>>()
        .expect("stdout is a stream of JSON documents");
    let report = docs.last().unwrap();
    let skipped = report["skipped"].as_array().unwrap();
    assert_eq!(skipped.len(), 1, "the present slot must be skipped");
    assert_eq!(skipped[0].as_str().unwrap(), "vibedeps/flow-wal/0.2.0");
    assert!(
        report["materialised"].as_array().unwrap().is_empty(),
        "nothing is freshly materialised on a no-op re-install"
    );
}

#[test]
fn install_reports_json() {
    let project = tempfile::tempdir().unwrap();
    init_project(project.path());

    let out = vibe()
        .arg("--json")
        .arg("install")
        .arg("org.vibevm.world/wal")
        .arg("--path")
        .arg(project.path())
        .arg("--registry")
        .arg(make_wal_dir_registry(project.path()))
        .arg("--assume-yes")
        .output()
        .unwrap();
    assert!(out.status.success());

    // The plan and the report are both emitted as JSON documents,
    // concatenated on stdout. Use StreamDeserializer to read every document
    // in order and inspect the last one (the install report).
    let stdout = String::from_utf8(out.stdout).unwrap();
    let de = serde_json::Deserializer::from_str(&stdout);
    let docs: Vec<serde_json::Value> = de
        .into_iter::<serde_json::Value>()
        .collect::<Result<_, _>>()
        .expect("stdout is a stream of JSON documents");
    assert!(docs.len() >= 2, "expected at least a plan and a report");
    let last = docs.last().unwrap();
    assert_eq!(last["ok"], true);
    assert_eq!(last["command"], "install");
    // PROP-009 install report: `materialised` lists the `vibedeps/`
    // slots written, `nodes_regenerated` the nodes whose boot artifacts
    // were rebuilt.
    let materialised = last["materialised"].as_array().unwrap();
    assert_eq!(materialised.len(), 1);
    assert_eq!(materialised[0].as_str().unwrap(), "vibedeps/flow-wal/0.2.0");
    assert_eq!(last["nodes_regenerated"].as_array().unwrap().len(), 1);
}

