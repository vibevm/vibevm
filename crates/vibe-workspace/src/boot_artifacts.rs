//! Generated boot artifacts — `STATIC.md`, `INDEX.md`, the redirect.
//!
//! PROP-009 §2.3. The computed-view engine ([`crate::boot`]) produces an
//! [`EffectiveBoot`]; this module projects it into the files an AI agent
//! reads at session start:
//!
//! - **`STATIC.md`** — the verbatim concatenation, in composed order, of
//!   every `static`-linked contribution. Read first — the priority lane.
//!   Generated only when the node has static contributions.
//! - **`INDEX.md`** — a generated TOML manifest: a `schema` version, an
//!   `static` pointer (when an `STATIC.md` exists), and an ordered list of
//!   `[[entry]]` tables, each with a `path` and a `kind` (`static` /
//!   `dynamic`). Always generated.
//! - **The redirect** — a managed `<vibevm>` block (PROP-012) inside each
//!   of `CLAUDE.md` / `AGENTS.md` / `GEMINI.md`, pointing a session at
//!   `STATIC.md` then `INDEX.md`. vibevm owns only the block; the rest of
//!   each file belongs to the developer and any other tool and is
//!   preserved verbatim.
//!
//! `STATIC.md` and `INDEX.md` are git-tracked and carry a "generated — do
//! not edit" header. Boot stays pure file-reading — the redirect points
//! at files, it never becomes "run `vibe`".
//!
//! ## `when` on dynamic entries
//!
//! A `[boot_snippet]` may carry a `when` condition (PROP-009 §2.4 / §2.6).
//! The computed-view engine renders such a contribution as a `dynamic`
//! entry, and [`render_index`] writes the condition into the `INDEX.md`
//! `[[entry]]` as `when = "os:<name>"`. The agent evaluates it at boot: it
//! reads a `dynamic` entry's file only when the condition holds for the
//! session's operating system. The committed `INDEX.md` is therefore
//! OS-invariant — every entry is listed, and the OS test happens at read
//! time. A `dynamic` entry without `when` is an unconditional INCLUDE —
//! still valid.

specmark::scope!("spec://vibevm/modules/vibe-workspace/PROP-009#artifacts");

use std::fs;
use std::path::{Path, PathBuf};

use serde::Serialize;
use specmark::spec;
use vibe_spec::{FileResolver, FsSectionSource, expand_embeds};

use crate::WorkspaceError;
use crate::boot::EffectiveBoot;

/// The host project's `spec://` authority (PROP-035 §6) — the root project is
/// addressed as `vibevm`. Used to resolve `#embed` targets when compiling the
/// inline lane.
const HOST_NAMESPACE: &str = "vibevm";

/// `schema` version of the generated `INDEX.md` manifest (PROP-009 §2.3).
pub const INDEX_SCHEMA: u32 = 1;

/// Generated-artifact filenames, relative to a node's `spec/boot/`.
pub const STATIC_FILE: &str = "STATIC.md";
/// See [`STATIC_FILE`].
pub const INDEX_FILE: &str = "INDEX.md";

/// The agent instruction files a node carries at its root. vibevm owns
/// only a `<vibevm>` block inside each (PROP-012); the rest of every file
/// belongs to the developer and any other tool sharing it.
pub const REDIRECT_FILES: &[&str] = &["CLAUDE.md", "AGENTS.md", "GEMINI.md"];

/// The opening marker of vibevm's managed block (PROP-012 §2.2), matched
/// as a whole trimmed line.
pub const BLOCK_OPEN: &str = "<vibevm>";

/// The closing marker of vibevm's managed block — see [`BLOCK_OPEN`].
pub const BLOCK_CLOSE: &str = "</vibevm>";

/// The header line of the pre-PROP-012 whole-file generated redirect. A
/// file carrying no `<vibevm>` markers but beginning with this line was
/// wholly vibevm's, and is migrated to a managed block wholesale
/// (PROP-012 §4).
const OLD_GENERATED_HEADER: &str =
    "<!-- Generated by vibe — do not edit. This file is overwritten on `vibe install`. -->";

const INDEX_HEADER: &str = "\
# spec/boot/INDEX.md — generated by vibe, do not edit.
# The computed boot sequence (PROP-009 §2.3). Read every file the
# `[[entry]]` list names, in order.
#
# A `kind = \"static\"` entry: read the file directly. A `kind = \"dynamic\"`
# entry: an INCLUDE resolved at boot — when it also carries
# `when = \"os:<name>\"`, read the file only if the session's operating
# system is <name> (windows / macos / linux), and skip it otherwise.

";

const STATIC_HEADER: &str = "\
<!-- spec/boot/STATIC.md — generated by vibe, do not edit. -->
<!-- The static boot lane (PROP-009 §2.3): the highest-priority -->
<!-- contributions, compiled in verbatim. Read this first, in full. -->

";

/// The body of vibevm's managed block — the text between the `<vibevm>`
/// markers. PROP-012 §2.5: the payload is the PROP-009 §2.3 redirect,
/// unchanged; only the envelope (a fenced region of a shared file) is new.
const BLOCK_BODY: &str = "\
<!-- Generated by vibe — do not edit inside this block; it is rewritten on `vibe install`. Text outside the <vibevm> markers is yours. -->

# Session boot

This project's boot sequence is computed by vibe (the PROP-009 loading
model). To begin a session, read these files in order:

1. `spec/boot/STATIC.md` — if it exists. The static (priority) lane: read it
   first and in full.
2. `spec/boot/INDEX.md` — a generated TOML manifest. Read every file named
   by its `[[entry]]` tables, in the listed order. A `kind = \"static\"`
   entry is read directly; a `kind = \"dynamic\"` entry is an INCLUDE
   resolved at boot, and one carrying a `when` condition is read only when
   that condition holds for the current session.

Boot is pure file-reading — there is nothing to execute.";

/// One `[[entry]]` of the generated `INDEX.md` manifest.
#[derive(Debug, Serialize)]
struct IndexEntry {
    /// Path of the boot file, as composed by the engine.
    path: String,
    /// `"static"` — read the file directly — or `"dynamic"` — an INCLUDE
    /// the agent resolves at boot.
    kind: &'static str,
    /// Activation condition for a `dynamic` entry (PROP-009 §2.3) — the
    /// wire string `"os:<name>"`. `None` on a `static` entry or an
    /// unconditional `dynamic` one.
    #[serde(skip_serializing_if = "Option::is_none")]
    when: Option<String>,
}

/// The serialised shape of `INDEX.md`.
#[derive(Debug, Serialize)]
struct IndexManifest {
    schema: u32,
    #[serde(rename = "static", skip_serializing_if = "Option::is_none")]
    static_lane: Option<String>,
    #[serde(rename = "entry", skip_serializing_if = "Vec::is_empty")]
    entries: Vec<IndexEntry>,
}

/// Render the `INDEX.md` TOML manifest for a node's effective boot.
///
/// Pure — the manifest is paths and kinds, no file content. The `static`
/// pointer is present exactly when `boot` has `static`-linked entries.
/// Serialisation is structurally infallible with today's fixed manifest
/// shape; the error is routed anyway so a future shape change degrades
/// to a diagnosis instead of a panic.
pub fn render_index(boot: &EffectiveBoot) -> Result<String, WorkspaceError> {
    let has_static = boot.static_entries().next().is_some();
    let manifest = IndexManifest {
        schema: INDEX_SCHEMA,
        static_lane: has_static.then(|| format!("spec/boot/{STATIC_FILE}")),
        entries: boot
            .dynamic_entries()
            .map(|e| IndexEntry {
                path: e.path.clone(),
                // Every INDEX entry is dynamically linked (read by reference);
                // its read-timing derives from `when` — a conditional entry is
                // a gated INCLUDE ("dynamic"), an unconditional one is read
                // directly ("static"). PROP-009 §2.3.
                kind: if e.when.is_some() {
                    "dynamic"
                } else {
                    "static"
                },
                when: e.when.map(|w| w.to_string()),
            })
            .collect(),
    };
    let body = toml::to_string_pretty(&manifest).map_err(|e| WorkspaceError::IndexRender {
        reason: e.to_string(),
    })?;
    Ok(format!("{INDEX_HEADER}{body}"))
}

/// Render `STATIC.md` — the verbatim concatenation of the `static`-linked
/// contributions, in composed order. Returns `None` when the node has no
/// static contributions (no `STATIC.md` is generated then).
///
/// `workspace_root` resolves each contribution's path to its content on
/// disk; the contributions live in the materialised `vibedeps/` tree.
pub fn render_static(
    boot: &EffectiveBoot,
    workspace_root: &Path,
) -> Result<Option<String>, WorkspaceError> {
    let entries: Vec<_> = boot.static_entries().collect();
    if entries.is_empty() {
        return Ok(None);
    }
    let mut out = String::from(STATIC_HEADER);
    for entry in entries {
        if entry.use_ref {
            // A soft-hoist reference (PROP-038 §2.5): this package's text lives
            // once in the global root STATIC.md. Leave a `#use` marker so the
            // graph edge survives locally and the read-set dedups the read —
            // the agent knows the package is part of this zone without a
            // duplicated copy.
            out.push_str(&format!(
                "<!-- vibe:hoisted {} — text in the root STATIC.md -->\n#use spec://{}\n\n",
                entry.origin, entry.origin
            ));
            continue;
        }
        let abs = workspace_root.join(&entry.path);
        let content = fs::read_to_string(&abs).map_err(|e| io_err(&abs, e))?;
        // An HTML-comment provenance marker — invisible in rendered
        // markdown, so the concatenated content stays verbatim.
        out.push_str(&format!(
            "<!-- vibe:static {} — {} -->\n\n",
            entry.origin, entry.path
        ));
        out.push_str(content.trim_end());
        out.push_str("\n\n");
    }
    // Spec-compiler (PROP-035 §8): expand any `#embed` the static lane carries
    // (§7.1). Guarded — a lane with no directives is byte-identical, so
    // vibevm's own boot stays untouched until it adopts the format (§15).
    // `#use` / `#source` are mode-dependent and left to the structural loader.
    if has_embed_directive(&out) {
        let source = FsSectionSource::new(FileResolver::new(workspace_root, HOST_NAMESPACE));
        out = expand_embeds(&out, &source).map_err(|e| WorkspaceError::InlineCompile {
            reason: e.to_string(),
        })?;
    }
    Ok(Some(out))
}

/// Whether the static lane carries an `#embed` directive (a line starting with
/// `#embed ` after leading whitespace) — the guard that keeps a directive-free
/// lane byte-identical.
fn has_embed_directive(text: &str) -> bool {
    text.lines().any(|l| l.trim_start().starts_with("#embed "))
}

/// The body of vibevm's managed redirect block — the text between the
/// `<vibevm>` markers (PROP-012 §2.5).
#[spec(
    implements = "spec://vibevm/modules/vibe-workspace/PROP-012#content",
    r = 1
)]
pub fn render_redirect() -> &'static str {
    BLOCK_BODY
}

/// The full managed block — the `<vibevm>` marker, the body, the
/// `</vibevm>` marker — as written into an instruction file. Ends with a
/// trailing newline so it slots cleanly into a file.
fn render_block() -> String {
    format!("{BLOCK_OPEN}\n{BLOCK_BODY}\n{BLOCK_CLOSE}\n")
}

/// Where vibevm's managed `<vibevm>` block sits in an instruction file
/// (PROP-012 §2.2–§2.3).
#[derive(Debug, Clone, PartialEq, Eq)]
#[spec(
    implements = "spec://vibevm/modules/vibe-workspace/PROP-012#markers",
    r = 1
)]
pub enum BlockLocation {
    /// No `<vibevm>` markers at all — the block is created on write (§2.3).
    Absent,
    /// Exactly one well-formed pair. `[start, end)` is the block's byte
    /// span — the first byte of the `<vibevm>` line to the byte after the
    /// `</vibevm>` line (its newline included when present).
    Present { start: usize, end: usize },
    /// The markers are malformed — not exactly one ordered pair. The
    /// string names the defect for the operator.
    Malformed(String),
}

/// Locate vibevm's managed block in `content` by a plain line scan: a
/// line whose trimmed text equals `<vibevm>` / `</vibevm>` is a marker
/// (PROP-012 §2.2). No markdown parse, no model — the detection that
/// gates a mutating write must be deterministic and trivial.
#[spec(
    implements = "spec://vibevm/modules/vibe-workspace/PROP-012#markers",
    r = 1
)]
pub fn locate_block(content: &str) -> BlockLocation {
    let mut open: Option<(usize, usize)> = None;
    let mut close: Option<(usize, usize)> = None;
    let mut opens = 0usize;
    let mut closes = 0usize;
    let mut offset = 0usize;
    for line in content.split_inclusive('\n') {
        let trimmed = line.strip_suffix('\n').unwrap_or(line).trim();
        if trimmed == BLOCK_OPEN {
            opens += 1;
            open.get_or_insert((offset, offset + line.len()));
        } else if trimmed == BLOCK_CLOSE {
            closes += 1;
            close.get_or_insert((offset, offset + line.len()));
        }
        offset += line.len();
    }
    // Matching on the recorded spans (not just the counts) lets the
    // compiler carry the "one of each → both recorded" invariant; the
    // impossible counts-without-spans combination falls through to the
    // Malformed arm instead of a panic.
    match (opens, closes, open, close) {
        (0, 0, _, _) => BlockLocation::Absent,
        (1, 1, Some((open_start, _)), Some((close_start, close_end))) => {
            if open_start < close_start {
                BlockLocation::Present {
                    start: open_start,
                    end: close_end,
                }
            } else {
                BlockLocation::Malformed(
                    "the `</vibevm>` marker precedes its `<vibevm>` opener".to_string(),
                )
            }
        }
        (opens, closes, _, _) => BlockLocation::Malformed(format!(
            "expected exactly one `<vibevm>` … `</vibevm>` pair, found {opens} `<vibevm>` \
             and {closes} `</vibevm>` marker line(s)"
        )),
    }
}

/// Write vibevm's managed block into the instruction file at `path`
/// (PROP-012 §2.3). Returns the path when the file changed, or `None`
/// when it already carried a byte-identical block (no write — no churn).
///
/// Absent markers → append a fresh block at end of file, preserving every
/// existing byte (or, when the whole file is the pre-PROP-012 generated
/// redirect, replace it wholesale — PROP-012 §4); a missing file is
/// created. Present → splice the new body between the markers, touching
/// nothing outside them. Malformed → [`WorkspaceError::MalformedRedirectBlock`]
/// without writing — callers validate at plan time (PROP-012 §2.4), so
/// this is the defensive last line.
#[spec(
    implements = "spec://vibevm/modules/vibe-workspace/PROP-012#create",
    r = 1
)]
#[spec(
    implements = "spec://vibevm/modules/vibe-workspace/PROP-012#migration",
    r = 1
)]
fn write_managed_block(path: &Path) -> Result<Option<PathBuf>, WorkspaceError> {
    let existing = match fs::read_to_string(path) {
        Ok(s) => Some(s),
        Err(e) if e.kind() == std::io::ErrorKind::NotFound => None,
        Err(e) => return Err(io_err(path, e)),
    };
    let block = render_block();
    let new_content = match &existing {
        None => block,
        Some(content) => match locate_block(content) {
            BlockLocation::Malformed(reason) => {
                return Err(WorkspaceError::MalformedRedirectBlock {
                    path: path.to_path_buf(),
                    reason,
                });
            }
            BlockLocation::Present { start, end } => {
                let mut out = String::with_capacity(content.len());
                out.push_str(&content[..start]);
                out.push_str(&block);
                out.push_str(&content[end..]);
                out
            }
            BlockLocation::Absent => {
                if content.trim().is_empty()
                    || content.trim_start().starts_with(OLD_GENERATED_HEADER)
                {
                    // An empty file, or the pre-PROP-012 whole-file
                    // generated redirect — the file was vibevm's, so
                    // reclaim it as a block (PROP-012 §4).
                    block
                } else {
                    // Co-tenant content — append the block after it,
                    // preserving every existing byte (PROP-012 §2.3).
                    let mut out = content.clone();
                    if !out.ends_with('\n') {
                        out.push('\n');
                    }
                    out.push('\n');
                    out.push_str(&block);
                    out
                }
            }
        },
    };
    if existing.as_deref() == Some(new_content.as_str()) {
        return Ok(None);
    }
    fs::write(path, &new_content).map_err(|e| io_err(path, e))?;
    Ok(Some(path.to_path_buf()))
}

/// Write vibevm's managed `<vibevm>` block into every agent instruction
/// file ([`REDIRECT_FILES`]) of the node rooted at `node_dir` (PROP-012).
/// Returns the files actually written — a file already carrying an
/// identical block is left untouched.
pub fn write_redirect_blocks(node_dir: &Path) -> Result<Vec<PathBuf>, WorkspaceError> {
    let mut written = Vec::new();
    for name in REDIRECT_FILES {
        if let Some(path) = write_managed_block(&node_dir.join(name))? {
            written.push(path);
        }
    }
    Ok(written)
}

/// What [`write_boot_artifacts`] wrote — for the caller to report.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct WrittenArtifacts {
    /// Absolute path of the generated `INDEX.md` — always written.
    pub index: PathBuf,
    /// Absolute path of the generated `STATIC.md`, when the node had
    /// static contributions; `None` otherwise.
    pub static_lane: Option<PathBuf>,
    /// Absolute paths of the instruction files whose `<vibevm>` block was
    /// written or updated this run; a file already carrying an identical
    /// block is not listed (PROP-012).
    pub redirects: Vec<PathBuf>,
}

/// Generate and write a node's boot artifacts (PROP-009 §2.3): `INDEX.md`
/// always, `STATIC.md` when the node has static contributions, and the
/// `CLAUDE.md` / `AGENTS.md` / `GEMINI.md` redirects.
///
/// `node_dir` is the node's root; `workspace_root` is the absolute
/// workspace root, used to resolve the static contributions' content. A
/// stale `STATIC.md` from an earlier generation is removed when the node
/// no longer has static contributions, so it is never read by mistake.
#[spec(
    implements = "spec://vibevm/modules/vibe-workspace/PROP-012#co-tenant",
    r = 1
)]
pub fn write_boot_artifacts(
    node_dir: &Path,
    workspace_root: &Path,
    boot: &EffectiveBoot,
) -> Result<WrittenArtifacts, WorkspaceError> {
    let boot_dir = node_dir.join("spec").join("boot");
    fs::create_dir_all(&boot_dir).map_err(|e| io_err(&boot_dir, e))?;

    // INDEX.md — always.
    let index = boot_dir.join(INDEX_FILE);
    fs::write(&index, render_index(boot)?).map_err(|e| io_err(&index, e))?;

    // STATIC.md — only when there are static contributions.
    let static_path = boot_dir.join(STATIC_FILE);
    let static_lane = match render_static(boot, workspace_root)? {
        Some(text) => {
            fs::write(&static_path, text).map_err(|e| io_err(&static_path, e))?;
            Some(static_path)
        }
        None => {
            if static_path.exists() {
                fs::remove_file(&static_path).map_err(|e| io_err(&static_path, e))?;
            }
            None
        }
    };

    // The redirects — vibevm's managed `<vibevm>` block in each (PROP-012).
    // Only the block is written; co-tenant content is preserved, and a
    // file already carrying an identical block is left untouched.
    let redirects = write_redirect_blocks(node_dir)?;

    Ok(WrittenArtifacts {
        index,
        static_lane,
        redirects,
    })
}

/// Build a [`WorkspaceError::Io`] from a `std::io::Error` and its path.
fn io_err(path: &Path, e: std::io::Error) -> WorkspaceError {
    WorkspaceError::Io {
        path: path.to_path_buf(),
        reason: e.to_string(),
    }
}

