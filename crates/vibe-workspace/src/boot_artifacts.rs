//! Generated boot artifacts — `INLINE.md`, `INDEX.md`, the redirect.
//!
//! PROP-009 §2.3. The computed-view engine ([`crate::boot`]) produces an
//! [`EffectiveBoot`]; this module projects it into the files an AI agent
//! reads at session start:
//!
//! - **`INLINE.md`** — the verbatim concatenation, in composed order, of
//!   every `inline`-linked contribution. Read first — the priority lane.
//!   Generated only when the node has inline contributions.
//! - **`INDEX.md`** — a generated TOML manifest: a `schema` version, an
//!   `inline` pointer (when an `INLINE.md` exists), and an ordered list of
//!   `[[entry]]` tables, each with a `path` and a `kind` (`static` /
//!   `dynamic`). Always generated.
//! - **The redirect** — `CLAUDE.md` / `AGENTS.md` / `GEMINI.md`, identical
//!   thin files pointing a session at `INLINE.md` then `INDEX.md`.
//!
//! Both `INLINE.md` and `INDEX.md` are git-tracked and carry a
//! "generated — do not edit" header. Boot stays pure file-reading — the
//! redirect points at files, it never becomes "run `vibe`".
//!
//! This module is additive: nothing calls it yet. Workspace-aware
//! `vibe install` wires it in at a later PROP-009 phase, and the vibevm
//! self-migration rewrites this repository's own entry-point files then.
//!
//! ## `when` on dynamic entries
//!
//! PROP-009 §2.3 shows a `when` activation condition on a `dynamic`
//! `INDEX.md` entry, but §2.6 pins no manifest field that declares it.
//! [`IndexEntry`] carries an optional `when` so the manifest shape is
//! forward-compatible; it stays unset until the contract fixes where a
//! dynamic boot contribution's condition is declared. A `dynamic` entry
//! without `when` is an unconditional INCLUDE — still valid.

use std::fs;
use std::path::{Path, PathBuf};

use serde::Serialize;
use vibe_core::manifest::LinkType;

use crate::WorkspaceError;
use crate::boot::EffectiveBoot;

/// `schema` version of the generated `INDEX.md` manifest (PROP-009 §2.3).
pub const INDEX_SCHEMA: u32 = 1;

/// Generated-artifact filenames, relative to a node's `spec/boot/`.
pub const INLINE_FILE: &str = "INLINE.md";
/// See [`INLINE_FILE`].
pub const INDEX_FILE: &str = "INDEX.md";

/// The session-boot redirect filenames a node carries at its root —
/// identical content in each, one per agent harness.
pub const REDIRECT_FILES: &[&str] = &["CLAUDE.md", "AGENTS.md", "GEMINI.md"];

const INDEX_HEADER: &str = "\
# spec/boot/INDEX.md — generated by vibe, do not edit.
# The computed boot sequence (PROP-009 §2.3). Read every file the
# `[[entry]]` list names, in order.

";

const INLINE_HEADER: &str = "\
<!-- spec/boot/INLINE.md — generated by vibe, do not edit. -->
<!-- The inline boot lane (PROP-009 §2.3): the highest-priority -->
<!-- contributions, concatenated verbatim. Read this first, in full. -->

";

const REDIRECT_BODY: &str = "\
<!-- Generated by vibe — do not edit. This file is overwritten on `vibe install`. -->

# Session boot

This project's boot sequence is computed by vibe (the PROP-009 loading
model). To begin a session, read these files in order:

1. `spec/boot/INLINE.md` — if it exists. The priority lane: read it first
   and in full.
2. `spec/boot/INDEX.md` — a generated TOML manifest. Read every file named
   by its `[[entry]]` tables, in the listed order. A `kind = \"dynamic\"`
   entry is an INCLUDE to resolve at boot; a `kind = \"static\"` entry is
   read directly.

Boot is pure file-reading — there is nothing to execute.
";

/// One `[[entry]]` of the generated `INDEX.md` manifest.
#[derive(Debug, Serialize)]
struct IndexEntry {
    /// Path of the boot file, as composed by the engine.
    path: String,
    /// `"static"` — read the file directly — or `"dynamic"` — an INCLUDE
    /// the agent resolves at boot.
    kind: &'static str,
    /// Activation condition for a `dynamic` entry (PROP-009 §2.3). Unset
    /// until the contract pins its declaration site — see the module docs.
    #[serde(skip_serializing_if = "Option::is_none")]
    when: Option<String>,
}

/// The serialised shape of `INDEX.md`.
#[derive(Debug, Serialize)]
struct IndexManifest {
    schema: u32,
    #[serde(skip_serializing_if = "Option::is_none")]
    inline: Option<String>,
    #[serde(rename = "entry", skip_serializing_if = "Vec::is_empty")]
    entries: Vec<IndexEntry>,
}

/// Render the `INDEX.md` TOML manifest for a node's effective boot.
///
/// Pure — the manifest is paths and kinds, no file content. The `inline`
/// pointer is present exactly when `boot` has `inline`-linked entries.
pub fn render_index(boot: &EffectiveBoot) -> String {
    let has_inline = boot.inline_entries().next().is_some();
    let manifest = IndexManifest {
        schema: INDEX_SCHEMA,
        inline: has_inline.then(|| format!("spec/boot/{INLINE_FILE}")),
        entries: boot
            .indexed_entries()
            .map(|e| IndexEntry {
                path: e.path.clone(),
                kind: match e.link {
                    LinkType::Static => "static",
                    LinkType::Dynamic => "dynamic",
                    LinkType::Inline => {
                        unreachable!("inline entries are excluded by indexed_entries()")
                    }
                },
                when: None,
            })
            .collect(),
    };
    let body = toml::to_string_pretty(&manifest)
        .expect("the INDEX manifest is a fixed struct and always serialises");
    format!("{INDEX_HEADER}{body}")
}

/// Render `INLINE.md` — the verbatim concatenation of the `inline`-linked
/// contributions, in composed order. Returns `None` when the node has no
/// inline contributions (no `INLINE.md` is generated then).
///
/// `workspace_root` resolves each contribution's path to its content on
/// disk; the contributions live in the materialised `vibedeps/` tree.
pub fn render_inline(
    boot: &EffectiveBoot,
    workspace_root: &Path,
) -> Result<Option<String>, WorkspaceError> {
    let inline: Vec<_> = boot.inline_entries().collect();
    if inline.is_empty() {
        return Ok(None);
    }
    let mut out = String::from(INLINE_HEADER);
    for entry in inline {
        let abs = workspace_root.join(&entry.path);
        let content = fs::read_to_string(&abs).map_err(|e| io_err(&abs, e))?;
        // An HTML-comment provenance marker — invisible in rendered
        // markdown, so the concatenated content stays verbatim.
        out.push_str(&format!(
            "<!-- vibe:inline {} — {} -->\n\n",
            entry.origin, entry.path
        ));
        out.push_str(content.trim_end());
        out.push_str("\n\n");
    }
    Ok(Some(out))
}

/// The redirect content for `CLAUDE.md` / `AGENTS.md` / `GEMINI.md` — a
/// thin generated file pointing a session at the boot artifacts.
pub fn render_redirect() -> &'static str {
    REDIRECT_BODY
}

/// What [`write_boot_artifacts`] wrote — for the caller to report.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct WrittenArtifacts {
    /// Absolute path of the generated `INDEX.md` — always written.
    pub index: PathBuf,
    /// Absolute path of the generated `INLINE.md`, when the node had
    /// inline contributions; `None` otherwise.
    pub inline: Option<PathBuf>,
    /// Absolute paths of the generated redirect files.
    pub redirects: Vec<PathBuf>,
}

/// Generate and write a node's boot artifacts (PROP-009 §2.3): `INDEX.md`
/// always, `INLINE.md` when the node has inline contributions, and the
/// `CLAUDE.md` / `AGENTS.md` / `GEMINI.md` redirects.
///
/// `node_dir` is the node's root; `workspace_root` is the absolute
/// workspace root, used to resolve the inline contributions' content. A
/// stale `INLINE.md` from an earlier generation is removed when the node
/// no longer has inline contributions, so it is never read by mistake.
pub fn write_boot_artifacts(
    node_dir: &Path,
    workspace_root: &Path,
    boot: &EffectiveBoot,
) -> Result<WrittenArtifacts, WorkspaceError> {
    let boot_dir = node_dir.join("spec").join("boot");
    fs::create_dir_all(&boot_dir).map_err(|e| io_err(&boot_dir, e))?;

    // INDEX.md — always.
    let index = boot_dir.join(INDEX_FILE);
    fs::write(&index, render_index(boot)).map_err(|e| io_err(&index, e))?;

    // INLINE.md — only when there are inline contributions.
    let inline_path = boot_dir.join(INLINE_FILE);
    let inline = match render_inline(boot, workspace_root)? {
        Some(text) => {
            fs::write(&inline_path, text).map_err(|e| io_err(&inline_path, e))?;
            Some(inline_path)
        }
        None => {
            if inline_path.exists() {
                fs::remove_file(&inline_path).map_err(|e| io_err(&inline_path, e))?;
            }
            None
        }
    };

    // The redirects — identical content in each.
    let mut redirects = Vec::with_capacity(REDIRECT_FILES.len());
    for name in REDIRECT_FILES {
        let path = node_dir.join(name);
        fs::write(&path, render_redirect()).map_err(|e| io_err(&path, e))?;
        redirects.push(path);
    }

    Ok(WrittenArtifacts {
        index,
        inline,
        redirects,
    })
}

/// Build a [`WorkspaceError::Io`] from a `std::io::Error` and its path.
fn io_err(path: &Path, e: std::io::Error) -> WorkspaceError {
    WorkspaceError::Io {
        path: path.to_path_buf(),
        reason: e.to_string(),
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::boot::{BootBand, BootEntry};
    use tempfile::TempDir;

    fn entry(path: &str, link: LinkType, origin: &str) -> BootEntry {
        BootEntry {
            path: path.to_string(),
            band: BootBand::Dependency,
            link,
            origin: origin.to_string(),
        }
    }

    fn boot(entries: Vec<BootEntry>) -> EffectiveBoot {
        EffectiveBoot { entries }
    }

    #[test]
    fn render_index_of_empty_boot_is_just_the_schema() {
        let text = render_index(&boot(vec![]));
        let parsed: toml::Value = toml::from_str(&text).unwrap();
        assert_eq!(parsed.get("schema").unwrap().as_integer(), Some(1));
        assert!(parsed.get("inline").is_none());
        assert!(parsed.get("entry").is_none());
    }

    #[test]
    fn render_index_carries_static_and_dynamic_entries_in_order() {
        let b = boot(vec![
            entry("spec/boot/00-core.md", LinkType::Static, "."),
            entry(
                "vibedeps/stack-rust/2.1.0/boot/rust.md",
                LinkType::Dynamic,
                "stack:rust",
            ),
        ]);
        let text = render_index(&b);
        assert!(text.contains("generated by vibe, do not edit"));
        let parsed: toml::Value = toml::from_str(&text).unwrap();
        let entries = parsed.get("entry").unwrap().as_array().unwrap();
        assert_eq!(entries.len(), 2);
        assert_eq!(
            entries[0].get("path").unwrap().as_str(),
            Some("spec/boot/00-core.md")
        );
        assert_eq!(entries[0].get("kind").unwrap().as_str(), Some("static"));
        assert_eq!(entries[1].get("kind").unwrap().as_str(), Some("dynamic"));
    }

    #[test]
    fn render_index_inline_pointer_present_only_with_inline_entries() {
        let without = render_index(&boot(vec![entry(
            "spec/boot/00-core.md",
            LinkType::Static,
            ".",
        )]));
        assert!(!without.contains("inline ="));

        let with = render_index(&boot(vec![entry(
            "vibedeps/flow-crit/1.0.0/boot.md",
            LinkType::Inline,
            "flow:crit",
        )]));
        let parsed: toml::Value = toml::from_str(&with).unwrap();
        assert_eq!(
            parsed.get("inline").unwrap().as_str(),
            Some("spec/boot/INLINE.md")
        );
    }

    #[test]
    fn render_inline_is_none_without_inline_entries() {
        let ws = TempDir::new().unwrap();
        let b = boot(vec![entry("spec/boot/00-core.md", LinkType::Static, ".")]);
        assert!(render_inline(&b, ws.path()).unwrap().is_none());
    }

    #[test]
    fn render_inline_concatenates_contributions_verbatim() {
        let ws = TempDir::new().unwrap();
        let crit = ws.path().join("vibedeps/flow-crit/1.0.0/boot.md");
        fs::create_dir_all(crit.parent().unwrap()).unwrap();
        fs::write(&crit, "# Critical discipline\n\nAlways do the thing.").unwrap();

        let b = boot(vec![entry(
            "vibedeps/flow-crit/1.0.0/boot.md",
            LinkType::Inline,
            "flow:crit",
        )]);
        let text = render_inline(&b, ws.path()).unwrap().unwrap();
        assert!(text.contains("generated by vibe, do not edit"));
        assert!(text.contains("<!-- vibe:inline flow:crit"));
        assert!(text.contains("# Critical discipline"));
        assert!(text.contains("Always do the thing."));
    }

    #[test]
    fn render_inline_errors_on_a_missing_contribution() {
        let ws = TempDir::new().unwrap();
        let b = boot(vec![entry(
            "vibedeps/flow-gone/1.0.0/boot.md",
            LinkType::Inline,
            "flow:gone",
        )]);
        let err = render_inline(&b, ws.path()).unwrap_err();
        assert!(matches!(err, WorkspaceError::Io { .. }), "{err}");
    }

    #[test]
    fn render_redirect_points_at_the_boot_files() {
        let r = render_redirect();
        assert!(r.contains("do not edit"));
        assert!(r.contains("spec/boot/INLINE.md"));
        assert!(r.contains("spec/boot/INDEX.md"));
    }

    #[test]
    fn write_boot_artifacts_writes_index_and_redirects() {
        let ws = TempDir::new().unwrap();
        let b = boot(vec![entry("spec/boot/00-core.md", LinkType::Static, ".")]);
        let written = write_boot_artifacts(ws.path(), ws.path(), &b).unwrap();

        assert!(written.index.is_file());
        assert!(written.inline.is_none());
        assert!(!ws.path().join("spec/boot/INLINE.md").exists());
        assert_eq!(written.redirects.len(), 3);
        for name in REDIRECT_FILES {
            assert!(ws.path().join(name).is_file(), "{name} must be written");
        }
    }

    #[test]
    fn write_boot_artifacts_writes_inline_when_present() {
        let ws = TempDir::new().unwrap();
        let crit = ws.path().join("vibedeps/flow-crit/1.0.0/boot.md");
        fs::create_dir_all(crit.parent().unwrap()).unwrap();
        fs::write(&crit, "# discipline").unwrap();

        let b = boot(vec![entry(
            "vibedeps/flow-crit/1.0.0/boot.md",
            LinkType::Inline,
            "flow:crit",
        )]);
        let written = write_boot_artifacts(ws.path(), ws.path(), &b).unwrap();
        assert!(written.inline.is_some());
        assert!(ws.path().join("spec/boot/INLINE.md").is_file());
    }

    #[test]
    fn write_boot_artifacts_removes_a_stale_inline() {
        let ws = TempDir::new().unwrap();
        let crit = ws.path().join("vibedeps/flow-crit/1.0.0/boot.md");
        fs::create_dir_all(crit.parent().unwrap()).unwrap();
        fs::write(&crit, "# discipline").unwrap();

        // First generation has an inline contribution.
        let with_inline = boot(vec![entry(
            "vibedeps/flow-crit/1.0.0/boot.md",
            LinkType::Inline,
            "flow:crit",
        )]);
        write_boot_artifacts(ws.path(), ws.path(), &with_inline).unwrap();
        assert!(ws.path().join("spec/boot/INLINE.md").exists());

        // A later generation has none — the stale INLINE.md must go.
        let without = boot(vec![entry("spec/boot/00-core.md", LinkType::Static, ".")]);
        let written = write_boot_artifacts(ws.path(), ws.path(), &without).unwrap();
        assert!(written.inline.is_none());
        assert!(!ws.path().join("spec/boot/INLINE.md").exists());
    }
}
